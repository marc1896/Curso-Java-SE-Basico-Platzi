public class SwitchStatement {
    public static void main(String[] args) {
        int opt  = 1;

        switch (opt){
            case 1:
                System.out.println("Opcion 1");
                break;
            case 2:
                System.out.println("Opcion 2");
                break;
            case 3:
                System.out.println("Opcion 3");
                break;
        }
    }
}
